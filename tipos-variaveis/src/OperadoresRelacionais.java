public class OperadoresRelacionais {
/*Operadores relacionais avaliam  a relação entre duas  expressões e/ou variáveis.*/

    public static void main(String[] args) {
        int a = 10;
        int b = 15;
    //== Verifica se as variáveis são iguais.
        if (a==b) {
            System.out.println("Verdadeiro");
        }else {
            System.out.println("Falso");
        }
    //!= Verifica se uma variável é diferente da outra
        if (a!=b) {
            System.out.println("Verdadeiro");
        }else {
            System.out.println("Falso");
        }
    //> Verifica se uma variavel é maior que a outra
        
        if (a > b) {
            System.out.println("Verdadeiro");
        }else {
            System.out.println("Falso");
        }

    //< Verifica se uma variavel é menor que a outra
        if (a < b) {
            System.out.println("Verdadeiro");
        }else {
            System.out.println("Falso");
        }

    // >= Verifica se uma variavel é maior ou igual que a outra
        if (a >= b) {
            System.out.println("Verdadeiro");
        }else {
            System.out.println("Falso");
        }

    // <= Verifica se uma variavel é menor ou igual que a outra
        if (a >= b) {
            System.out.println("Verdadeiro");
        }else {
            System.out.println("Falso");
        }
    }
}
